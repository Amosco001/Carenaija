API_COST_ESTIMATES = {
    "nearby_search": 0.032,
    "place_details_basic": 0.017,
    "place_details_contact": 0.003,
    "place_details_atmosphere": 0.005,
    "text_search": 0.032,
    "photo": 0.007,
}

def estimate_discovery_cost(num_cities: int = 36, num_queries: int = 14, avg_results_per_search: int = 20):
    searches = num_cities * num_queries
    search_cost = searches * API_COST_ESTIMATES["nearby_search"]
    
    estimated_places = searches * avg_results_per_search * 0.5
    details_cost = estimated_places * (API_COST_ESTIMATES["place_details_basic"] + API_COST_ESTIMATES["place_details_contact"])
    
    photo_cost = estimated_places * API_COST_ESTIMATES["photo"] * 3
    
    total = search_cost + details_cost + photo_cost
    
    return {
        "searches": searches,
        "search_cost": round(search_cost, 2),
        "estimated_places": int(estimated_places),
        "details_cost": round(details_cost, 2),
        "photo_cost": round(photo_cost, 2),
        "total_estimated_cost": round(total, 2),
        "monthly_cost_daily_runs": round(total * 30, 2),
    }
